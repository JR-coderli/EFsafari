#!/bin/bash

###############################################################################
# 手动运行 ETL 的脚本
###############################################################################

PROJECT_DIR="/opt/bicode"

# 使用昨天的日期
DATE=$(date -d 'yesterday' +'%Y-%m-%d')

# 或者指定日期（取消注释并修改）
# DATE="2026-01-09"

echo "=========================================="
echo "  运行 ETL 任务"
echo "  日期: $DATE"
echo "=========================================="
echo ""

cd "$PROJECT_DIR/backend"
python3 run_etl.py -d "$DATE"

echo ""
echo "ETL 完成！查看日志: tail -f /var/log/bicode/etl.log"
